package ru.netology.util;

public class SubmitByNamePhone {

    private final String name;
    private final String phone;

    public SubmitByNamePhone(String name, String phone) {
        this.name = name;
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }
}